package JavaAdvanceoopsconcepts;

public class Inheritancedemo1 {
	
	int age = 25;
	int empid = 125;
	String name = "Raghu";
	
	public void test1() {
		System.out.println("the age of test 1 is :" +age);
	}
	
	private void test2() {
		System.out.println("The empid of test2 is :" +empid);
	}
	
	protected void test3() {
		System.out.println("the name of test3 is :" +name);
	}
	
	

	public static void main(String[] args) {
		

	}

}
